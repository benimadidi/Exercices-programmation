
def PI() :
    return 3.14

def perimetre_carre(cote):
    return 4 * cote
def aire_carre(cote):
    return cote**2

def volume_cube(cote):
    return cote**3
 
def perimetre_cercle(rayon):
    return 2 * PI() * rayon
def aire_cercle(rayon):
    return PI() * rayon**2

def volume_sphere(rayon):

    return (4/3) * PI() * rayon**3
 